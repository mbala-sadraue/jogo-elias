// Função para obter desafios com base na categoria e tipo
export function getExpandedChallenges(category: CategoryType, type?: ChallengeType): GameChallenge[] {
  let challenges: GameChallenge[] = [];
  
  switch (category) {
    case "suave":
      challenges = [...suavePerguntas, ...suaveDesafios];
      break;
    case "picante":
      challenges = [...picantePerguntas, ...picanteDesafios];
      break;
    case "selvagem":
      challenges = [...selvagemPerguntas, ...selvagemDesafios];
      break;
    case "extremo":
      challenges = [...extremoPerguntas, ...extremoDesafios];
      break;
    default:
      challenges = [...suavePerguntas, ...suaveDesafios];
  }
  
  if (type) {
    return challenges.filter(c => c.type === type);
  }
  
  return challenges;
}

// Função para obter um desafio aleatório
export function getRandomExpandedChallenge(category: CategoryType, type?: ChallengeType): GameChallenge {
  const filteredChallenges = getExpandedChallenges(category, type);
  const randomIndex = Math.floor(Math.random() * filteredChallenges.length);
  return filteredChallenges[randomIndex];
}

